/*--------------------------------------------------------------------------------------*/

int image_alloc(image_t *image, size_t x, size_t y)
{
	image->x = x;
	image->y = y;
	image->stride = x;
	image->pixels = (pixel_t *)memory_allocate(sizeof(pixel_t) * x * y);
	if(image->pixels == NULL)
		return MEMORY_ERROR;
	return 0;
}

void image_free(image_t *image)
{
	memory_free(image->pixels);
}

