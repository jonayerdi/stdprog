/*--------------------------------------------------------------------------------------*/
/*                                       INCLUDES                                       */
/*--------------------------------------------------------------------------------------*/
#include "config/idle_loop.h"

#include "SDL.h"

/*--------------------------------------------------------------------------------------*/
/*                            PUBLIC FUNCTION DECLARATIONS                              */
/*--------------------------------------------------------------------------------------*/

void idle_loop(void)
{
    SDL_Event event;
    volatile char stop = 0;
    while(!stop)
    {
        while(SDL_PollEvent(&event))
        {
            if(event.type == SDL_QUIT)
            {
                /**
                 * @todo Also stop SDL threads somehow
                 */
                stop = 1;
            }
        }
        SDL_Delay(10);
    }
}

